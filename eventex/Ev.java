    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        if(edit.getText()=="Edit")
        {
        n.setEditable(true);
        c.setEditable(true);
        des.setEditable(true);
        p.setEditable(true);
        d.setEditable(true);
        edit.setText("Save");
        }
        else
        {
            event.setCategory(c.getText());
            event.setName(n.getText());
            event.setDate(d.getText());
            event.setDescription(des.getText());
            event.setPlace(p.getText());
            Statement ss;
            try {
                ss = start.conn.createStatement();
                String sql = "update Event "
				+ "set Name = \'"+event.getName()
                        + "\' , Place = \' "+event.getPlace()
                        + "\' , Date = \' "+event.getDate()
                        + "\'  , Category = \' "+event.getCategory()
                        + "\'  , Description = \' "+event.getDescription()+"\'";
                        
                ss.executeUpdate(sql);
                Notification not = new Notification(event.getEventID(),"The Event "+event.getName()+" has been updated");
                String sql1 = "update Attended "
				+ "set EventName = \'"+event.getName();
                ss.executeUpdate(sql1);
                String sql2 = "Select UserId from Attended where EventId = "+Ev.event.getEventID();
                ResultSet rs = ss.executeQuery(sql2);
                ArrayList<Integer> ids = new   ArrayList<Integer>();
                while(rs.next())
                {
                    ids.add(Integer.parseInt(rs.getString("UserId")));
                }
                not.storeNot(ids);
            } catch (SQLException ex) {
                Logger.getLogger(Ev.class.getName()).log(Level.SEVERE, null, ex);
            }
            edit.setText("Edit");
             JOptionPane.showMessageDialog(null, "The event is updated !");
            
                    
        }
        
        
    }//GEN-LAST:event_editActionPerformed

