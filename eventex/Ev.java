    public Ev(int eventid) {
        initComponents();
        conn = start.conn;
        us = User_Int.us;
        try {
        Statement stat = conn.createStatement();
        //where Name = \""+ na + "\" AND Password = \""+ pa + "\""
        String sql = " Select * from Event where Id = "+eventid;
         ResultSet rs = stat.executeQuery(sql);
        rs.next();
        String ss = rs.getString("Owner");
         
         int id = Integer.parseInt(ss);
          int number = Integer.parseInt(rs.getString("Attendees"));
         event = new  Event(rs.getString("Name"), rs.getString("Category"), rs.getString("Date"), rs.getString("Description"),rs.getString("Place"),id);
        
         event.setNo_of_attendees(number);
         event.setEventID   (eventid);
         n.setText(event.getName());
        c.setText(event.getCategory());
        d.setText(event.getDate());
        des.setText(event.getDescription());
        p.setText(event.getPlace());
        String sql1 = " Select Name from User where Id = "+id;
         ResultSet rs1 = stat.executeQuery(sql1);
        if(rs1.next())
        {
        String own_name = rs1.getString("Name");
        own.setText("Owner: "+ own_name );
        }
        no.setText("Number of attendees: "+  number);
        if(event.getOwnerID()==us.getUserID())
        {
            att.setEnabled(false);
           
        }
        else
        {
        String sql2 = "Select UserId from Attended where EventId = "+eventid+" AND UserId = "+us.getUserID();
        //System.out.println(sql2);
         ResultSet rs28 = stat.executeQuery(sql2);
          if(rs28.next())
          {
              att.setEnabled(false);
          }
        edit.setVisible(false);
        Del.setVisible(false);
        }

        } catch (Exception exc){
            exc.printStackTrace();   
        }

    }

